/*******************************************************************************
 * Function name  : DRIVERS_Init
 * Interface      : uint8_t DRIVERS_Init(void)
 * Description    : Init the system peripherals drivers
 * Arguments      : None
 * Return value   : None
*******************************************************************************/
uint8_t DRIVERS_Init(void)
{
    uint8_t result;
    
    do 
    {
        result = CORE_Init();
        result = RCC_Init();
        result = GPIO_Init();
        result = USART_Init();

    } while (result != 0);
}